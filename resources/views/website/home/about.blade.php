    <div class="text-sm py-4">
        <p class="my-2">
            In February 2017, the African Union Commission Citizens and Diaspora Directorate (AUC-CIDO) and the
            Deutsche Gesellschaft für Internationale Zusammenarbeit (GIZ) GmbH, jointly launched a pilot project -
            the Interfaith Dialogue on Violent Extremism (iDove). This initiative stands out due to its emphasis on
            the soft power of religion and innovative youth-led approaches in PVE. iDove is an intercontinental
            project which specifically targets youth from Africa and Europe (age 18-35). In 2019, the project
            further expanded to the Middle East and Southeast Asia.
        </p>
        <p class="my-2">
            The rise of violent extremism has raised concerns about growing fragility, instability, and prejudice
            that divide communities worldwide. Violent extremism has, thus, become one of the main challenges to
            peaceful coexistence which pose a crucial threat to the goals and aspirations of the 2030 Agenda for
            Sustainable Development as well as the 2063 Agenda: The Africa We Want.
        </p>
        <p class="my-2">
            iDove is based on the understanding that violent extremism is a global challenge. Whether it has
            political, ideological, or religious roots, extremism is a problem that affects societies globally in
            similar ways. Security interventions to address the problem only have limited effects and come at a high
            cost, both financially and in human lives. PVE, therefore, requires a comprehensive ‘soft’ approach that
            focuses on the root causes rather than on the symptoms of violent extremism. Sports, arts, and cultural
            activities offer effective means of dealing with differences within a community. Bottom-up, innovative
            community-based approaches in the areas of academia, arts and culture, or communication often foster
            mutual understanding, support, and cooperation between different social and religious groups.
        </p>
